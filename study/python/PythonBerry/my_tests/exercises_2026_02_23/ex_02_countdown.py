# Упражнение 2: Обратный отсчёт
# ------------------------------
# Создай список чисел от 10 до 1 с помощью range().
# Выведи каждое число в цикле.
# После цикла выведи: "Поехали!"
# Подсказка: range(start, stop, step) — step может быть отрицательным.

for n in range(10, 0, -1):
    print(n)

print("Поехали!")
