# Упражнение 1: Чётное или нечётное
# ---------------------------------
# Пройдись циклом for по числам от 1 до 20.
# Для каждого числа выведи сообщение: "<число> - чётное" или "<число> - нечётное"
# Подсказка: используй оператор % для проверки на чётность.

for n in range(1, 21):
    if n % 2 == 0:
        print(n, "- чётное")
    else:
        print(n, "- не чётное")
