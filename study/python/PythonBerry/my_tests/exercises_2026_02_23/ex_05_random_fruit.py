# Упражнение 5: Случайный фрукт
# ------------------------------
# Создай список из 5 фруктов.
# С помощью random.choice() выбери случайный фрукт.
# Выведи сообщение: "Сегодня ты ешь: <фрукт>"
# Подсказка: не забудь импортировать random

import random

fruit = ["Банан", "Ананас", "Апельсин", "Яблоко", "Грейпфрут"]

print("Сегодня ты ешь: ", random.choice(fruit))
